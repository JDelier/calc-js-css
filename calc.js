
export function soma(n1,n2) {
 
    return  n1 + n2
   
}

export function subtracao(n1, n2) {
    const result = n1 - n2
    return result
}

export  function multiplicacao(n1, n2) {

    const result = n1 * n2
    return result

}

export function divisao(n1, n2) {

    const result = n1 / n2
    return result

}
